#include "libft.h"
#include <stdlib.h>
#include <string.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	while (i < size)
	{
		dst[i] = src[i];
		i++;
	}
	return (i);
}
int	main(void)
{
	char dst[] = "deneme";
	const char src[5] = "osman";
	printf("%ld", ft_strlcpy(dst, src, 2));
}